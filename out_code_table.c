#ifdef SKFDEBUG
      if ((is_v_debug) && !is_ucs_ufam(conv_cap)) 
	fprintf(stderr,"G0 charset: %s  ",
	 ((ti < 0) ?  "-" : iso_ubytedef_table[ti].ientry[ii].desc));
#endif
      ti = ocodeset->g0adef.tbl_index;
