/*  latin < cjk < kanji < hangle. Other definitions are ignored.   */
/* --------------------------------------------------------------- */
/* input: parse - output codeset index in ocodeset		   */
/* output: none (ignored if failed)				   */
/* side_effect: g*_table_mod, g*_* are set, conv_cap is set	   */
/* --------------------------------------------------------------- */
